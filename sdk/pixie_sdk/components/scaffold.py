"""Generate scaffold HTML + TypeScript definition files for labeling components.

Creates a ready-to-use ``.html`` file and a companion ``.d.ts`` type
definition file in the components directory, based on a test suite's
name and input schema fetched from the remote pixie-server.

Both files are named after the **normalized** test suite name (snake_case).

Usage::

    from pixie_sdk.components.scaffold import scaffold_component

    html_path, dts_path = await scaffold_component(
        test_suite_id=UUID("adf79684-..."),
        components_dir=Path("labeling"),
    )

See Also:
    ``scanner.scan_and_register`` — discovers scaffolded files at startup.
    ``RemoteClient.get_test_suite`` — fetches test suite metadata.
"""

from __future__ import annotations

import re
from pathlib import Path
from typing import Any
from uuid import UUID

from pixie_sdk.components import PLACEHOLDER_ATTR
from pixie_sdk.remote_client import RemoteClient

# ============================================================================
# HTML Template
# ============================================================================

_HTML_TEMPLATE = """\
<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8" />
  <title>Manual Labeling | {suite_name}</title>
</head>
<body>
  <div id="app-container"></div>
  <script {placeholder_attr}>
    window.INPUT=undefined;
  </script>
  <script>
    // The INPUT variable is injected by the pixie-evals framework.
    // See {dts_file_name} for the TypeScript type definition.
    // Customize this page to display your test case data for labeling.
    const container = document.getElementById('app-container');
    const preElement = document.createElement('pre')
    preElement.textContent = JSON.stringify(window.INPUT, undefined, 2);
  </script>
</body>
</html>
"""

# ============================================================================
# TypeScript Definition Template
# ============================================================================

_DTS_TEMPLATE = """\
// {dts_file_name}
// Type definitions for test suite: {suite_name}
// Generated by: pixie scaffold
//
// These types describe the shape of the INPUT variable that the
// pixie-evals framework injects into your HTML labeling page.
// Use them for editor autocompletion and type checking if you
// develop with TypeScript-aware tooling.

interface InputProps {{
{fields}
}}

/**
 * The input object injected by the pixie-evals framework.
 * Available as a global variable in your labeling HTML page.
 */
declare const INPUT: InputProps;
"""


# ============================================================================
# Helpers
# ============================================================================


def _to_snake_case(name: str) -> str:
    """Convert a human-readable name to snake_case.

    Examples:
        >>> _to_snake_case("Trace Comparison")
        'trace_comparison'
        >>> _to_snake_case("LLM Output Review")
        'llm_output_review'
    """
    slug = re.sub(r"[^a-zA-Z0-9]+", "_", name)
    slug = re.sub(r"([a-z0-9])([A-Z])", r"\1_\2", slug)
    return slug.strip("_").lower()


def _json_schema_to_ts_type(schema: dict[str, Any]) -> str:
    """Map a JSON Schema type to a TypeScript type string.

    Handles basic types and arrays.  Nested objects fall back to
    ``Record<string, unknown>``.

    Args:
        schema: A JSON Schema property definition with a ``type`` field.

    Returns:
        TypeScript type string.
    """
    json_type = schema.get("type", "unknown")

    if json_type == "string":
        return "string"
    if json_type == "number" or json_type == "integer":
        return "number"
    if json_type == "boolean":
        return "boolean"
    if json_type == "array":
        items = schema.get("items", {})
        inner = _json_schema_to_ts_type(items) if items else "unknown"
        return f"{inner}[]"
    if json_type == "object":
        return "Record<string, unknown>"
    return "unknown"


def _build_fields(input_schema: dict[str, Any]) -> str:
    """Build the TypeScript interface fields from a JSON Schema ``properties`` map.

    Args:
        input_schema: JSON Schema object (``type: "object"``).

    Returns:
        Indented TypeScript field declarations (one per line).
    """
    properties = input_schema.get("properties", {})
    required = set(input_schema.get("required", []))

    if not properties:
        return "  // TODO: Add fields matching your input objects\n  [key: string]: unknown;"

    lines: list[str] = []
    for field_name, field_schema in properties.items():
        ts_type = _json_schema_to_ts_type(field_schema)
        optional = "" if field_name in required else "?"
        lines.append(f"  {field_name}{optional}: {ts_type};")
    return "\n".join(lines)


# ============================================================================
# Public Interface
# ============================================================================


async def scaffold_component(
    test_suite_id: UUID,
    components_dir: Path,
    *,
    remote_client: RemoteClient | None = None,
) -> tuple[Path, Path]:
    """Generate a scaffold HTML labeling page and companion ``.d.ts`` file.

    Both files are named after the **normalized** (snake_case) test suite
    name so that the SDK server serves the page at
    ``/labeling/{normalized_name}?id=...``.

    Fetches the test suite name and ``input_schema`` from the remote
    pixie-server, converts the schema properties into a TypeScript
    ``InputProps`` interface.

    Args:
        test_suite_id: UUID of the test suite on the remote server.
        components_dir: Directory to write the files into.
            Created automatically if it doesn't exist.
        remote_client: Optional pre-configured remote client.
            If ``None``, a new ``RemoteClient()`` is created.

    Returns:
        Tuple of ``(html_path, dts_path)`` — absolute paths to the
        created files.

    Raises:
        ValueError: If the test suite is not found on the remote server.
    """
    client = remote_client or RemoteClient()
    suite = await client.get_test_suite(test_suite_id)

    if suite is None:
        raise ValueError(f"Test suite {test_suite_id} not found on the remote server.")

    suite_name: str = suite.get("name", "LabelingPage")
    snake_name = _to_snake_case(suite_name)

    # Extract input schema from config
    config = suite.get("config") or {}
    if isinstance(config, str):
        import json

        config = json.loads(config)
    input_schema: dict[str, Any] = (
        config.get("input_schema") or config.get("inputSchema") or {}
    )

    # Build TypeScript fields
    fields = _build_fields(input_schema)

    # File names — both use the normalized suite name
    html_file_name = f"{snake_name}.html"
    dts_file_name = f"{snake_name}.d.ts"

    # Render HTML
    html_content = _HTML_TEMPLATE.format(
        suite_name=suite_name,
        placeholder_attr=PLACEHOLDER_ATTR,
        dts_file_name=dts_file_name,
    )

    # Render d.ts
    dts_content = _DTS_TEMPLATE.format(
        dts_file_name=dts_file_name,
        suite_name=suite_name,
        fields=fields,
    )

    # Write files
    components_dir.mkdir(parents=True, exist_ok=True)
    html_path = components_dir / html_file_name
    dts_path = components_dir / dts_file_name

    html_path.write_text(html_content, encoding="utf-8")
    dts_path.write_text(dts_content, encoding="utf-8")

    return html_path, dts_path
