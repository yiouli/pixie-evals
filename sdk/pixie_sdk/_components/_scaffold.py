"""Generate scaffold TSX files for labeling components.

Creates a ready-to-use ``.tsx`` file in the components directory based on
a test suite's name and input schema fetched from the remote pixie-server.

Usage::

    from pixie_sdk._components._scaffold import scaffold_component

    path = await scaffold_component(
        test_suite_id=UUID("adf79684-..."),
        components_dir=Path("labeling"),
    )

See Also:
    ``_scanner.scan_and_register`` — discovers scaffolded files at startup.
    ``RemoteClient.get_test_suite`` — fetches test suite metadata.
"""

from __future__ import annotations

import re
from pathlib import Path
from typing import Any
from uuid import UUID

from pixie_sdk.remote_client import RemoteClient

# ============================================================================
# TSX Template
# ============================================================================

_TSX_TEMPLATE = """\
// {file_name}
// Test suite: {suite_name}
// Generated by: pixie scaffold
// The route will be served at: /labeling/{slot_name}?id=<data_entry_id>

// The props interface IS the data contract between this component and the framework.
// The framework passes the input object loaded by ID directly as these props.
// Field names must match the keys in your stored input objects.
export interface InputProps {{
{fields}
}}

// Customize this component to display your test case data for labeling.
export default function {component_name}(props: InputProps) {{
  return (
    <div>
      <h2>{suite_name}</h2>
      <pre>{{JSON.stringify(props, null, 2)}}</pre>
    </div>
  );
}}
"""


# ============================================================================
# Helpers
# ============================================================================


def _to_snake_case(name: str) -> str:
    """Convert a human-readable name to snake_case for the file/slot name.

    Examples:
        >>> _to_snake_case("Trace Comparison")
        'trace_comparison'
        >>> _to_snake_case("LLM Output Review")
        'llm_output_review'
    """
    # Replace non-alphanumeric characters with underscores
    slug = re.sub(r"[^a-zA-Z0-9]+", "_", name)
    # Insert underscores before uppercase letters (camelCase → camel_case)
    slug = re.sub(r"([a-z0-9])([A-Z])", r"\1_\2", slug)
    return slug.strip("_").lower()


def _to_pascal_case(snake: str) -> str:
    """Convert a snake_case string to PascalCase.

    Examples:
        >>> _to_pascal_case("trace_comparison")
        'TraceComparison'
    """
    return "".join(part.capitalize() for part in snake.split("_"))


def _json_schema_to_ts_type(schema: dict[str, Any]) -> str:
    """Map a JSON Schema type to a TypeScript type string.

    Handles basic types and arrays.  Nested objects fall back to
    ``Record<string, unknown>``.

    Args:
        schema: A JSON Schema property definition with a ``type`` field.

    Returns:
        TypeScript type string.
    """
    json_type = schema.get("type", "unknown")

    if json_type == "string":
        return "string"
    if json_type == "number" or json_type == "integer":
        return "number"
    if json_type == "boolean":
        return "boolean"
    if json_type == "array":
        items = schema.get("items", {})
        inner = _json_schema_to_ts_type(items) if items else "unknown"
        return f"{inner}[]"
    if json_type == "object":
        return "Record<string, unknown>"
    return "unknown"


def _build_fields(input_schema: dict[str, Any]) -> str:
    """Build the TypeScript interface fields from a JSON Schema ``properties`` map.

    Args:
        input_schema: JSON Schema object (``type: "object"``).

    Returns:
        Indented TypeScript field declarations (one per line).
    """
    properties = input_schema.get("properties", {})
    required = set(input_schema.get("required", []))

    if not properties:
        return "  // TODO: Add fields matching your input objects\n  [key: string]: unknown;"

    lines: list[str] = []
    for field_name, field_schema in properties.items():
        ts_type = _json_schema_to_ts_type(field_schema)
        optional = "" if field_name in required else "?"
        lines.append(f"  {field_name}{optional}: {ts_type};")
    return "\n".join(lines)


# ============================================================================
# Public Interface
# ============================================================================


async def scaffold_component(
    test_suite_id: UUID,
    components_dir: Path,
    *,
    remote_client: RemoteClient | None = None,
) -> Path:
    """Generate a scaffold TSX component for a test suite.

    The file is named ``{test_suite_id}.tsx`` so that the SDK server
    automatically serves it at ``/labeling/{test_suite_id}?id=...``.
    The frontend uses the test suite ID to construct the iframe URL,
    so no additional configuration is needed.

    Fetches the test suite name and ``input_schema`` from the remote
    pixie-server, converts the schema properties into a TypeScript
    ``InputProps`` interface, and writes the file into *components_dir*.

    Args:
        test_suite_id: UUID of the test suite on the remote server.
        components_dir: Directory to write the ``.tsx`` file into.
            Created automatically if it doesn't exist.
        remote_client: Optional pre-configured remote client.
            If ``None``, a new ``RemoteClient()`` is created.

    Returns:
        Absolute path to the created ``.tsx`` file.

    Raises:
        ValueError: If the test suite is not found on the remote server.
    """
    client = remote_client or RemoteClient()
    suite = await client.get_test_suite(test_suite_id)

    if suite is None:
        raise ValueError(f"Test suite {test_suite_id} not found on the remote server.")

    # Derive human-readable name for the React function (PascalCase)
    suite_name: str = suite.get("name", "LabelingComponent")
    pascal_name = _to_pascal_case(_to_snake_case(suite_name))

    # The file/slot name is the test suite UUID — this is the key that
    # connects frontend → server → component.
    slot_name = str(test_suite_id)

    # Extract input schema from config
    config = suite.get("config") or {}
    if isinstance(config, str):
        import json

        config = json.loads(config)
    # The remote server returns "input_schema" (snake_case).
    # Accept both variants so the scaffold works regardless of which
    # serialisation convention the server uses.
    input_schema: dict[str, Any] = (
        config.get("input_schema") or config.get("inputSchema") or {}
    )

    # Build fields
    fields = _build_fields(input_schema)

    # Render TSX
    file_name = f"{slot_name}.tsx"
    content = _TSX_TEMPLATE.format(
        file_name=file_name,
        slot_name=slot_name,
        suite_name=suite_name,
        component_name=pascal_name,
        fields=fields,
    )

    # Write file
    components_dir.mkdir(parents=True, exist_ok=True)
    out_path = components_dir / file_name
    out_path.write_text(content, encoding="utf-8")

    return out_path
